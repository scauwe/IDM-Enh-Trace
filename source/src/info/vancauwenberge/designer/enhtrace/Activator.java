package info.vancauwenberge.designer.enhtrace;

import java.util.HashMap;
import java.util.Map;

import org.eclipse.core.runtime.ILog;
import org.eclipse.core.runtime.Status;
import org.eclipse.jface.resource.ImageDescriptor;
import org.eclipse.swt.graphics.Image;
import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 */
public class Activator extends AbstractUIPlugin {
	

	// The plug-in ID
	public static final String PLUGIN_ID = "info.vancauwenberge.designer.enhtrace"; //$NON-NLS-1$

	// The shared instance
	private static Activator plugin;
	private static Map<String, Image> imageCache = new HashMap<String, Image>();
	/**
	 * The constructor
	 */
	public Activator() {
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#start(org.osgi.framework.BundleContext)
	 */
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#stop(org.osgi.framework.BundleContext)
	 */
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	/**
	 * Returns the shared instance
	 *
	 * @return the shared instance
	 */
	public static Activator getDefault() {
		return plugin;
	}

	
	
	/**
	 * Returns an image descriptor for the image file at the given
	 * plug-in relative path
	 *
	 * @param path the path
	 * @return the image descriptor
	 */
	public static ImageDescriptor getImageDescriptor(String path) {
		return imageDescriptorFromPlugin(PLUGIN_ID, path);
	}
	
	public static void log(String msg) {
		Activator.log(msg, null);
	   }
	
	public static void log(String msg, Throwable e) {
		Status status = null;
		if (e!= null)
			status = new Status(Status.ERROR, plugin.getBundle().getSymbolicName(), Status.OK, msg, e);
		else
			status = new Status(Status.OK, plugin.getBundle().getSymbolicName(), Status.OK, msg, e);
		
		synchronized (PLUGIN_ID) {
			ILog log = null;
			if (plugin != null)
				log  = plugin.getLog();
			if (log != null)
				log.log(status);			
		}
	}

}
